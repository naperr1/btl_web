package Modal;

/**
 *
 * @author Fattt
 */
public class Item {
    private String itemID, name;
    private int stock;
    private double costPrice, sellPrice;
    private String image, description, categoryID;

    public Item() {
    }

    public Item(String itemID, String name, int stock, double costPrice, double sellPrice, String image, String description, String categoryID) {
        this.itemID = itemID;
        this.name = name;
        this.stock = stock;
        this.costPrice = costPrice;
        this.sellPrice = sellPrice;
        this.image = image;
        this.description = description;
        this.categoryID = categoryID;
    }

    public String getItemID() {
        return itemID;
    }

    public void setItemID(String itemID) {
        this.itemID = itemID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public double getCostPrice() {
        return costPrice;
    }

    public void setCostPrice(double costPrice) {
        this.costPrice = costPrice;
    }

    public double getSellPrice() {
        return sellPrice;
    }

    public void setSellPrice(double sellPrice) {
        this.sellPrice = sellPrice;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCategoryID() {
        return categoryID;
    }

    public void setCategoryID(String categoryID) {
        this.categoryID = categoryID;
    }
    
}
